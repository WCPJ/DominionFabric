package org.worldcraft.dominioncraft.nation;

/**
 * Права действий на уровне нации.
 *
 * <p>Это НЕ игровые блок-права (build/break) – их контролируют города.
 * Нация управляет политическими и административными процессами:
 * приём городов, выход, статус (open/close), конституция и голосования.</p>
 *
 * <p>Фактический набор разрешений по каждому {@link NationRank} хранится
 * в {@code Nation.rankPerms}. Лидер по умолчанию обладает ВСЕМИ правами,
 * вне зависимости от содержимого rankPerms.</p>
 */
public enum NationPermission {

    /* ───────── управление составом ───────── */

    /** Приглашать города или голосовать за их вступление (закрытая нация). */
    INVITE_TOWN,

    /** Утверждать заявки городов (используется для голосов совета). */
    APPROVE_APPLICATION,

    /** Утверждать выход города (монарх / совет). */
    APPROVE_SECESSION,

    /** Принудительно исключить город из нации (админ мера). */
    FORCE_REMOVE_TOWN,

    /* ───────── статус ───────── */

    /** Менять режим OPEN/CLOSED. */
    MANAGE_STATUS,

    /* ───────── конституция ───────── */

    /**
     * Предлагать / вносить изменения в текст конституции.
     * Реальное применение зависит от флага
     * {@link ConstitutionKey#EDIT_CONSTITUTION_ALLOWED}.
     */
    MANAGE_CONSTITUTION,

    /* ───────── голосования ───────── */

    /** Голосовать в совете (используется для утверждения заявок, налогов и т.п.). */
    VOTE_COUNCIL,

    /** Голосовать в общенациональных референдумах. */
    VOTE_REFERENDUM,

    /** Запускать референдум (инициатор). */
    START_REFERENDUM,

    /* ───────── ранги ───────── */

    /** Назначать ранги игрокам внутри нации. */
    GRANT_RANK,

    /* ───────── резерв под будущее ───────── */

    /** Зарезервировано под расширения / административный override. */
    ADMIN_OVERRIDE;
}
