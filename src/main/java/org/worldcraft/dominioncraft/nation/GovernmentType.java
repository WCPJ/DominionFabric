package org.worldcraft.dominioncraft.nation;

/**
 * Форма правления нации.
 *
 * <ul>
 *   <li>{@link #MONARCHY}   – пожизненный монарх. Смена лидера возможна
 *       только при импичменте или передаче престола.</li>
 *   <li>{@link #REPUBLIC}   – лидер выбирается через регулярные выборы.
 *       Период задаётся в NBT поля {@code Election} внутри {@link Nation}.</li>
 *   <li>{@link #CONFEDERATION} – loose‑структура: у городов максимум
 *       автономии; большинство решений требует единогласия (зарезервировано
 *       «на будущее», на этот момент логика равна республике).</li>
 * </ul>
 *
 * <p>От формы правления зависит процесс выхода города, выборов
 * и наложения ветто на законы конституции.</p>
 */
public enum GovernmentType {

    /** Монархия — выход города только с утверждения монарха
     *  и без обязательных референдумов. */
    MONARCHY,

    /** Республика — ключевые решения (налог, выход города, изменение
     *  конституции) требуют референдума либо голосования совета. */
    REPUBLIC,

    /** Конфедерация — максимально свободная структура. Зарезервировано
     *  под расширения; поведение можно переопределить в конституции. */
    CONFEDERATION
}
