package org.worldcraft.dominioncraft.nation;

/**
 * Ранги игроков внутри нации.
 *
 * <p>Ранг определяет набор прав  которые
 * хранятся в самой нации (см. Nation.rankPerms).</p>
 *
 * <ul>
 *   <li>{@link #LEADER} – глава государства (монарх / президент).</li>
 *   <li>{@link #MINISTER} – высшее руководство (совет, министры);
 *       участвуют в утверждении заявок городов и изменении статуса нации.</li>
 *   <li>{@link #REPRESENTATIVE} – делегаты городов; голосуют в совете.</li>
 *   <li>{@link #CITIZEN} – рядовой житель любого города-члена
 *       (участвует в референдумах, если разрешено).</li>
 * </ul>
 *
 * <p>NB: Конкретные права настраиваются в {@code Nation} и могут
 * быть расширены через конституционные флаги.</p>
 */
public enum NationRank {
    LEADER,
    MINISTER,
    REPRESENTATIVE,
    CITIZEN;
}
